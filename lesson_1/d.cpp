#include <iostream>

#include <cmath>

using namespace std;

int main(){
    // sqrt - square root, abs - |-5| = 5 - module, max(1 value, 2value), min(), pow(1 value, 2 value)
    cout << sqrt(10) << endl;
    cout << abs(-1000) << endl;
    cout << max(10, 12) << endl;
    cout << min(15, 20) << endl;
    cout << pow(5, 4) << endl;


    return 0;
}